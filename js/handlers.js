function chk() {
                    var checkBox = document.getElementById("myCheck");
                    var eqhide = document.getElementById("eqhide");
                    if (checkBox.checked == true){
                      eqhide.style.display = "none";
                    } else {
                      eqhide.style.display = "block";
                    }
                  }

